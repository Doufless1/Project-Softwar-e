let currentDevice = "lht-saxion";
let currentDataType = "insideTemperature";
let currentTimeRange = "day";

export function updateChartInfo(device, range, type) {
    currentDevice = device;
    currentDataType = type;
    currentTimeRange = range;
}


function getDate(range, deviceName){
    const currentTime = new Date(); 
    const timeRangeMinutes = timeRange[range]; 
    const numDataPoints = weatherData[deviceName].insideTemperature.day.length;
    const interval = timeRangeMinutes / numDataPoints; 

    
    const timeLabels = Array(numDataPoints).fill().map((_, i) => {
        const labelTime = new Date(currentTime.getTime() - (timeRangeMinutes - i * interval) * 60 * 1000);
        const date = labelTime.toLocaleDateString('en-GB'); 
        const time = labelTime.toTimeString().slice(0, 5); 
        return `${date} ${time}`;
    });
    return timeLabels
}

export function initializeChart() {
    const ctx = document.getElementById('myChart').getContext('2d');
    chart = new Chart(ctx, {
        type: 'line',
        data: {
            labels: getDate('day', 'lht-saxion'), 
            datasets: [] // Initially empty, no data will be shown
        },
        options: {
            maintainAspectRatio: false,
            responsive: true,
            scales: {
                y: {
                    beginAtZero: false
                }
            }
        }
    });
}

window.callApiJS = async function (endpoint) {
    try {
        const response = await fetch(`https://software-api.azurewebsites.net/api/graph/${endpoint}`);
        if (!response.ok) {
            throw new Error(`HTTP error! Status: ${response.status}`);
        }
        console.log('got the response for: ', endpoint );

       
        const apiData = await response.json();

        
        const device = endpoint;
        if (!weatherData[device]) {
            console.log('device doesnt exist: ', device);
            weatherData[device] = {};
        }
        if (!weatherData[device]) {
            console.log('couldnt create', device);
        }

        
        function filterInvalidValues(dataArray) {
            return dataArray.filter(value => value > -100);
        }

        
        weatherData[device].insideTemperature = {
            day: filterInvalidValues(apiData.HourlyDayInsideTemperatureAverage || []),
            week: filterInvalidValues(apiData.DailyWeekInsideTemperatureAverage || []),
            month: filterInvalidValues(apiData.DailyMonthInsideTemperatureAverage || [])
        };

        
        if (apiData.HourlyDayOutsideTemperatureAverage) {
            weatherData[device].outsideTemperature = {
                day: filterInvalidValues(apiData.HourlyDayOutsideTemperatureAverage || []),
                week: filterInvalidValues(apiData.DailyWeekOutsideTemperatureAverage || []),
                month: filterInvalidValues(apiData.DailyMonthOutsideTemperatureAverage || [])
            };
        }

        if (apiData.HourlyDayHumidityAverage) {
            weatherData[device].humidity = {
                day: filterInvalidValues(apiData.HourlyDayHumidityAverage || []),
                week: filterInvalidValues(apiData.DailyWeekHumidityAverage || []),
                month: filterInvalidValues(apiData.DailyMonthHumidityAverage || [])
            };
        }

        if (apiData.HourlyDayLightAverage) {
            weatherData[device].luminocity = {
                day: filterInvalidValues(apiData.HourlyDayLightAverage || []),
                week: filterInvalidValues(apiData.DailyWeekLightAverage || []),
                month: filterInvalidValues(apiData.DailyMonthLightAverage || [])
            };
        }

        if (apiData.HourlyDayPressureAverage) {
            weatherData[device].airPressure = {
                day: filterInvalidValues(apiData.HourlyDayPressureAverage || []),
                week: filterInvalidValues(apiData.DailyWeekPressureAverage || []),
                month: filterInvalidValues(apiData.DailyMonthPressureAverage || [])
            };
        }

       
        updateChart();

        console.log("Updated weatherData:", weatherData);
    } catch (error) {
        console.error("Error updating weather data:", error);
    }
};



function updateChart() {
    const device = currentDevice;
    const dataType = currentDataType;
    const timeRange = currentTimeRange;
    console.log("dev: , data: , time: ", device, dataType, timeRange);

    const selectedData = weatherData[device] && weatherData[device][dataType] && weatherData[device][dataType][timeRange];

    if (!selectedData) {
        console.error("Selected data is missing!");
        return;
    }



    chart.data.datasets.forEach((dataset) => {
        if (dataset.label === device) {
            
            dataset.data = selectedData;
        }
    });
    getDate(timeRange, device);

    // Update the chart to reflect the changes
    chart.update();
}


export function updateChartData(type, range) {
    chart.data.datasets = [];
      for (const city in activeCities) {
        if (activeCities[city]) {
          chart.data.datasets.push({
            label: city,
            data: weatherData[city][type][range],
            borderWidth: 1,
            borderColor: cityColors[city],
          });
            chart.data.labels = getDate(range, currentDevice);

        }
      }
      chart.update();
}

export function toggleCityData(city, type, range) {
    if (activeCities[city]) {
      chart.data.datasets = chart.data.datasets.filter(dataset => dataset.label !== city);
      activeCities[city] = false;
    }
    else {
        chart.data.datasets.push({
            label: city,
            data: weatherData[city][type][range],
            borderWidth: 1,
            borderColor: cityColors[city],
          });
        chart.update();
        activeCities[city] = true;
    }
    chart.update();
  }
